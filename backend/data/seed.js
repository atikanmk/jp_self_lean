async function seedDatabase() {
  try {
    const count = await Vocabulary.countDocuments();
    if (count === 0) {
      await Vocabulary.insertMany(sampleVocabulary);
      console.log(`Seeded database with ${sampleVocabulary.length} vocabulary items`);
    } else {
      console.log(`Database already has ${count} vocabulary items, skipping seed`);
    }
  } catch (err) {
    console.error('Error seeding database:', err);
  }
}

